package com.example.demo.domains.DTO.DTOCars;

public class CarPart {

    public CarPart() {}

    private String name;

    private String cost;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCost() {
        return cost;
    }

    public void setCost(String cost) {
        this.cost = cost;
    }

}
